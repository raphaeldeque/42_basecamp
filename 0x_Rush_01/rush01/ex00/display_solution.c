#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	display_solution(int solution[4][4])
{
	int	i;
	int	j;

	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
		{
			ft_putchar(solution[i][j] + 48);
			if (j == 3)
				ft_putchar('\n');
			else
				ft_putchar(' ');
		}
	}
}
